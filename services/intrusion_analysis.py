# MODULE 1 — HONEYPOT SERVICE MODULE
# Simulates fake SSH / HTTP services to attract attackers
# ══════════════════════════════════════════════════════════════════

class HoneypotService:
    """
    Creates fake network listeners on common ports.
    Any connection attempt is captured and logged.
    Runs in background threads so Flask stays free.
    """

    # Fake SSH banner (mimics OpenSSH)
    SSH_BANNER = b"SSH-2.0-OpenSSH_8.9p1 Ubuntu-3ubuntu0.6\r\n"

    # Fake HTTP 200 response (mimics a real login page)
    HTTP_FAKE_RESPONSE = (
        b"HTTP/1.1 200 OK\r\n"
        b"Content-Type: text/html\r\n"
        b"Server: Apache/2.4.52 (Ubuntu)\r\n"
        b"X-Powered-By: PHP/8.1\r\n\r\n"
        b"<html><body><h1>Admin Login</h1>"
        b"<form method='POST'><input name='user'/><input name='pass' type='password'/>"
        b"<button>Login</button></form></body></html>"
    )

    def __init__(self, host: str = "0.0.0.0"):
        self.host    = host
        self.running = False
        self.threads = []

    def _handle_ssh(self, conn, addr):
        """Handle a fake SSH connection."""
        ip, port = addr
        with app.app_context():
            if not DatabaseManager.is_blocked(ip):
                data = RequestCapture.capture_manual(
                    ip=ip, port=22, service="SSH",
                    path="/ssh", payload="SSH handshake attempt",
                    method="CONNECT", protocol="TCP"
                )
                log = IntrusionLogger.log(data)
                AlertGenerator.evaluate(log)
        try:
            conn.send(self.SSH_BANNER)
            conn.recv(256)   # absorb client response
        except Exception:
            pass
        finally:
            conn.close()

    def _handle_http(self, conn, addr):
        """Handle a fake HTTP connection."""
        ip, port = addr
        try:
            raw = conn.recv(2048).decode(errors="ignore")
            lines  = raw.split("\r\n")
            method = "GET"
            path   = "/"
            if lines:
                parts = lines[0].split(" ")
                if len(parts) >= 2:
                    method, path = parts[0], parts[1]
            # grab body if POST
            body = raw.split("\r\n\r\n", 1)[1][:512] if "\r\n\r\n" in raw else ""
        except Exception:
            method, path, body = "GET", "/", ""
        finally:
            with app.app_context():
                if not DatabaseManager.is_blocked(ip):
                    data = RequestCapture.capture_manual(
                        ip=ip, port=8080, service="HTTP-Honeypot",
                        path=path, payload=body,
                        method=method, protocol="HTTP"
                    )
                    log = IntrusionLogger.log(data)
                    AlertGenerator.evaluate(log)
            try:
                conn.send(self.HTTP_FAKE_RESPONSE)
            except Exception:
                pass
            conn.close()

    def _listen(self, port: int, handler):
        """Generic TCP listener loop."""
        try:
            sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
            sock.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
            sock.bind((self.host, port))
            sock.listen(10)
            sock.settimeout(1.0)
            print(f"[HONEYPOT] Listening on port {port}")
            while self.running:
                try:
                    conn, addr = sock.accept()
                    t = threading.Thread(target=handler, args=(conn, addr), daemon=True)
                    t.start()
                except socket.timeout:
                    continue
        except Exception as e:
            print(f"[HONEYPOT] Port {port} error: {e}")

    def start(self, ssh_port: int = 2222, http_port: int = 8080):
        """Start honeypot listeners in background threads."""
        self.running = True
        for port, handler in [(ssh_port, self._handle_ssh), (http_port, self._handle_http)]:
            t = threading.Thread(target=self._listen, args=(port, handler), daemon=True)
            t.start()
            self.threads.append(t)
        print(f"[HONEYPOT] Services active on ports {ssh_port} (SSH) and {http_port} (HTTP)")

    def stop(self):
        self.running = False
        print("[HONEYPOT] Stopped.")


