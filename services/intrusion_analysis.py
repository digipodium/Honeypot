# MODULE 2 — REQUEST CAPTURE MODULE
# Captures incoming request details (IP, port, path, payload, UA)
# ══════════════════════════════════════════════════════════════════

class RequestCapture:
    """
    Captures all incoming attacker request metadata.
    Called from honeypot routes and real Flask routes.
    """

    # Known malicious payload signatures
    SQLI_PATTERNS    = ["' OR", "1=1", "UNION SELECT", "DROP TABLE", "admin'--", "SLEEP("]
    XSS_PATTERNS     = ["<script>", "onerror=", "javascript:", "onmouseover=", "alert("]
    PATH_PATTERNS    = ["../", "..\\", "/etc/passwd", "/proc/", "cmd.exe", "/bin/bash"]
    SHELL_PATTERNS   = ["wget ", "curl ", ".php?", "exec(", "system(", "passthru("]
    CRED_PATTERNS    = ["/login", "/admin", "/wp-admin", "/auth", "/signin", "/api/token"]
    SCANNER_PATTERNS = ["/wp-", ".env", ".git", "backup", ".zip", "config.php", "phpinfo"]

    @staticmethod
    def classify_threat(path: str, payload: str = "") -> tuple:
        """
        Classify threat type and severity from path + payload.
        Returns (threat_type, severity).
        """
        combined = (path + " " + (payload or "")).upper()

        for p in RequestCapture.SQLI_PATTERNS:
            if p.upper() in combined:
                return ("SQL Injection", "critical")

        for p in RequestCapture.SHELL_PATTERNS:
            if p.upper() in combined:
                return ("Shell Probe", "critical")

        for p in RequestCapture.PATH_PATTERNS:
            if p.upper() in combined:
                return ("Path Traversal", "critical")

        for p in RequestCapture.XSS_PATTERNS:
            if p.upper() in combined:
                return ("XSS Attempt", "high")

        for p in RequestCapture.CRED_PATTERNS:
            if p.upper() in combined:
                return ("Credential Probe", "high")

        for p in RequestCapture.SCANNER_PATTERNS:
            if p.upper() in combined:
                return ("Scanner Probe", "medium")

        return ("Unknown Probe", "low")

    @staticmethod
    def capture_from_flask_request(req) -> dict:
        """
        Extract all relevant fields from a live Flask request object.
        """
        ip      = req.headers.get("X-Forwarded-For", req.remote_addr or "0.0.0.0").split(",")[0].strip()
        path    = req.path
        payload = req.get_data(as_text=True)[:512] or ""
        ua      = req.user_agent.string[:256]

        threat_type, severity = RequestCapture.classify_threat(path, payload)

        return {
            "ip_address":   ip,
            "port":         req.environ.get("SERVER_PORT", 80),
            "protocol":     "HTTPS" if req.is_secure else "HTTP",
            "service":      "HTTP",
            "method":       req.method,
            "path":         path,
            "payload":      payload[:512] if payload else None,
            "user_agent":   ua,
            "threat_type":  threat_type,
            "severity":     severity,
            "action_taken": "Blocked" if severity == "critical" else "Alerted" if severity == "high" else "Logged",
            "is_blocked":   severity == "critical",
        }

    @staticmethod
    def capture_manual(ip: str, port: int, service: str, path: str,
                       payload: str = "", method: str = "GET",
                       country: str = None, protocol: str = "TCP") -> dict:
        """
        Build a capture dict manually (used by honeypot services).
        """
        threat_type, severity = RequestCapture.classify_threat(path, payload)
        return {
            "ip_address":   ip,
            "port":         port,
            "protocol":     protocol,
            "service":      service,
            "method":       method,
            "path":         path,
            "payload":      payload[:512] if payload else None,
            "country":      country,
            "threat_type":  threat_type,
            "severity":     severity,
            "action_taken": "Blocked" if severity == "critical" else "Alerted",
            "is_blocked":   severity == "critical",
        }


