# MODULE 4 — INTRUSION ANALYSIS MODULE
# Detects patterns, calculates risk score, builds statistics
# ══════════════════════════════════════════════════════════════════

class IntrusionAnalyzer:
    """
    Core analysis engine. Processes logged intrusions to
    produce statistics, patterns, risk scores, and trends.
    """

    @staticmethod
    def get_summary(hours: int = 24) -> dict:
        """High-level stats for the dashboard stat cards."""
        logs       = DatabaseManager.get_logs_last_hours(hours)
        total      = len(logs)
        blocked    = sum(1 for l in logs if l.is_blocked)
        unique_ips = len({l.ip_address for l in logs})
        criticals  = sum(1 for l in logs if l.severity == "critical")

        return {
            "total_intrusions": total,
            "unique_attackers":  unique_ips,
            "blocked_requests":  blocked,
            "block_rate":        round(blocked / total * 100, 1) if total else 0,
            "critical_events":   criticals,
            "risk_score":        IntrusionAnalyzer.calculate_risk_score(logs),
        }

    @staticmethod
    def calculate_risk_score(logs: list) -> int:
        """
        0–100 risk score based on:
          - Volume of critical events (weight 40)
          - Unique attacker IPs      (weight 25)
          - Attack frequency         (weight 20)
          - Percentage blocked       (weight 15)
        """
        if not logs:
            return 0

        total     = len(logs)
        criticals = sum(1 for l in logs if l.severity == "critical")
        unique    = len({l.ip_address for l in logs})
        blocked   = sum(1 for l in logs if l.is_blocked)
        block_pct = (blocked / total) if total else 0

        score = (
            min(criticals / max(total, 1) * 100, 100) * 0.40 +
            min(unique / 50, 1) * 100                 * 0.25 +
            min(total / 200, 1) * 100                 * 0.20 +
            (1 - block_pct) * 100                     * 0.15
        )
        return round(min(score, 100))

    @staticmethod
    def get_risk_label(score: int) -> str:
        if score >= 80: return "CRITICAL RISK"
        if score >= 60: return "HIGH RISK"
        if score >= 40: return "MEDIUM RISK"
        return "LOW RISK"

    @staticmethod
    def threat_type_distribution(logs: list) -> list:
        """Count of each threat type — for donut/bar chart."""
        counter = Counter(l.threat_type or "Unknown" for l in logs)
        total   = sum(counter.values()) or 1
        return [
            {"type": t, "count": c, "pct": round(c / total * 100, 1)}
            for t, c in counter.most_common()
        ]

    @staticmethod
    def top_attacker_ips(logs: list, limit: int = 10) -> list:
        """Top IPs by hit count with their dominant threat type."""
        ip_counter    = Counter(l.ip_address for l in logs)
        ip_threat_map = defaultdict(Counter)
        ip_sev_map    = defaultdict(list)
        ip_country    = {}

        for l in logs:
            ip_threat_map[l.ip_address][l.threat_type or "Unknown"] += 1
            ip_sev_map[l.ip_address].append(l.severity)
            if l.country:
                ip_country[l.ip_address] = l.country

        result = []
        for ip, count in ip_counter.most_common(limit):
            dominant_type = ip_threat_map[ip].most_common(1)[0][0]
            sevs          = ip_sev_map[ip]
            # worst severity this IP produced
            sev_order     = ["critical", "high", "medium", "low"]
            worst_sev     = next((s for s in sev_order if s in sevs), "low")
            result.append({
                "ip":       ip,
                "hits":     count,
                "country":  ip_country.get(ip, "Unknown"),
                "type":     dominant_type,
                "severity": worst_sev,
                "blocked":  DatabaseManager.is_blocked(ip),
            })
        return result

    @staticmethod
    def hourly_heatmap(logs: list) -> list:
        """
        Returns a 24-element list of event counts per hour
        for the last 24 hours — used by the heatmap widget.
        """
        counts = [0] * 24
        for l in logs:
            counts[l.timestamp.hour] += 1
        return counts

    @staticmethod
    def recent_events(logs: list, limit: int = 12) -> list:
        """Latest N events for the timeline panel."""
        sorted_logs = sorted(logs, key=lambda l: l.timestamp, reverse=True)
        return [l.to_dict() for l in sorted_logs[:limit]]

    @staticmethod
    def detect_patterns(logs: list) -> list:
        """
        Detect behavioural patterns across all logs.
        Returns a list of human-readable insight strings.
        """
        insights = []
        if not logs:
            return insights

        ip_counter = Counter(l.ip_address for l in logs)
        top_ip, top_count = ip_counter.most_common(1)[0]
        if top_count >= 20:
            insights.append({
                "level":   "danger",
                "title":   f"Heavy attacker: {top_ip}",
                "message": f"Single IP responsible for {top_count} events. Consider immediate block.",
            })

        type_counter = Counter(l.threat_type for l in logs)
        dominant_type, dom_count = type_counter.most_common(1)[0]
        total = len(logs)
        if dom_count / total > 0.4:
            insights.append({
                "level":   "warning",
                "title":   f"Dominant attack type: {dominant_type}",
                "message": f"{dom_count} of {total} events ({round(dom_count/total*100)}%) are {dominant_type}.",
            })

        recent = [l for l in logs if l.timestamp >= datetime.utcnow() - timedelta(hours=1)]
        if len(recent) > 50:
            insights.append({
                "level":   "danger",
                "title":   "Traffic spike detected",
                "message": f"{len(recent)} events in the last hour — possible DDoS or automated scan.",
            })

        criticals = [l for l in logs if l.severity == "critical"]
        if criticals:
            crit_ips = {l.ip_address for l in criticals}
            insights.append({
                "level":   "danger",
                "title":   f"{len(criticals)} critical events from {len(crit_ips)} IP(s)",
                "message": "Immediate review recommended.",
            })

        block_rate = sum(1 for l in logs if l.is_blocked) / total
        if block_rate > 0.85:
            insights.append({
                "level":   "success",
                "title":   "Block rate excellent",
                "message": f"{round(block_rate*100)}% of intrusions were blocked automatically.",
            })

        return insights

    @staticmethod
    def severity_trend(logs: list) -> dict:
        """
        Breakdown of severity counts — for stacked bar chart.
        Grouped by hour for the last 24 hours.
        """
        buckets = defaultdict(lambda: {"critical": 0, "high": 0, "medium": 0, "low": 0})
        for l in logs:
            h = l.timestamp.strftime("%H:00")
            sev = l.severity if l.severity in ("critical","high","medium","low") else "low"
            buckets[h][sev] += 1
        return dict(sorted(buckets.items()))


