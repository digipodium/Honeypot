class IntrusionLog(db.Model):
    """
    Stores every captured intrusion attempt.
    Populated by the Request Capture Module and
    persisted by the Intrusion Logging Module.
    """
    __tablename__ = "intrusion_logs"

    id            = db.Column(db.Integer, primary_key=True)
    timestamp     = db.Column(db.DateTime, default=datetime.utcnow, index=True)
    ip_address    = db.Column(db.String(45), nullable=False, index=True)
    port          = db.Column(db.Integer, nullable=True)
    protocol      = db.Column(db.String(10), default="TCP")
    service       = db.Column(db.String(20), default="HTTP")   # SSH / HTTP / FTP
    method        = db.Column(db.String(10), default="GET")
    path          = db.Column(db.String(512), nullable=True)
    payload       = db.Column(db.Text, nullable=True)
    country       = db.Column(db.String(64), nullable=True)
    threat_type   = db.Column(db.String(50), nullable=True)    # SQLi / Brute / etc.
    severity      = db.Column(db.String(10), default="medium") # critical/high/medium/low
    action_taken  = db.Column(db.String(20), default="Logged") # Blocked / Alerted / Logged
    is_blocked    = db.Column(db.Boolean, default=False)
    user_agent    = db.Column(db.String(256), nullable=True)
    session_id    = db.Column(db.String(64), nullable=True)    # honeypot session

    def to_dict(self):
        return {
            "id":           self.id,
            "timestamp":    self.timestamp.strftime("%Y-%m-%d %H:%M:%S"),
            "ip_address":   self.ip_address,
            "port":         self.port,
            "protocol":     self.protocol,
            "service":      self.service,
            "method":       self.method,
            "path":         self.path,
            "payload":      self.payload,
            "country":      self.country,
            "threat_type":  self.threat_type,
            "severity":     self.severity,
            "action_taken": self.action_taken,
            "is_blocked":   self.is_blocked,
            "user_agent":   self.user_agent,
        }


