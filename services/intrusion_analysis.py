class IntrusionLogger:
    """
    Records every intrusion attempt to the database.
    All events pass through here before any analysis.
    """

    @staticmethod
    def log(capture_data: dict) -> IntrusionLog:
        """Log one intrusion event. Returns the saved record."""
        log_entry = DatabaseManager.insert_intrusion(capture_data)
        print(f"[LOG] {log_entry.timestamp} | {log_entry.ip_address} | "
              f"{log_entry.threat_type} | {log_entry.severity.upper()}")
