# MODULE 7 — DATABASE MANAGEMENT HELPERS
# Insert / retrieve / delete operations
# ══════════════════════════════════════════════════════════════════

class DatabaseManager:
    """Handles all database CRUD operations."""

    @staticmethod
    def insert_intrusion(data: dict) -> IntrusionLog:
        """Insert a new intrusion record."""
        log = IntrusionLog(**data)
        db.session.add(log)
        db.session.commit()
        return log

    @staticmethod
    def get_all_logs(limit: int = 500) -> list:
        return IntrusionLog.query.order_by(IntrusionLog.timestamp.desc()).limit(limit).all()

    @staticmethod
    def get_logs_last_hours(hours: int = 24) -> list:
        since = datetime.utcnow() - timedelta(hours=hours)
        return IntrusionLog.query.filter(IntrusionLog.timestamp >= since).all()

    @staticmethod
    def get_logs_by_ip(ip: str) -> list:
        return IntrusionLog.query.filter_by(ip_address=ip).order_by(IntrusionLog.timestamp.desc()).all()

    @staticmethod
    def delete_log(log_id: int) -> bool:
        log = IntrusionLog.query.get(log_id)
        if log:
            db.session.delete(log)
            db.session.commit()
            return True
        return False

    @staticmethod
    def block_ip(ip: str, reason: str = "Manual block", by: str = "admin"):
        existing = BlockedIP.query.filter_by(ip_address=ip).first()
        if not existing:
            entry = BlockedIP(ip_address=ip, reason=reason, blocked_by=by)
            db.session.add(entry)
            # mark all logs from this IP as blocked
            IntrusionLog.query.filter_by(ip_address=ip).update({"is_blocked": True, "action_taken": "Blocked"})
            db.session.commit()
        return True

    @staticmethod
    def is_blocked(ip: str) -> bool:
        return BlockedIP.query.filter_by(ip_address=ip).first() is not None

    @staticmethod
    def insert_alert(data: dict) -> Alert:
        alert = Alert(**data)
        db.session.add(alert)
        db.session.commit()
        return alert

    @staticmethod
    def get_unread_alerts() -> list:
        return Alert.query.filter_by(is_read=False).order_by(Alert.timestamp.desc()).all()

    @staticmethod
    def mark_alert_read(alert_id: int):
        alert = Alert.query.get(alert_id)
        if alert:
            alert.is_read = True
            db.session.commit()

    @staticmethod
    def purge_old_logs(days: int = 30):
        """Delete logs older than N days."""
        cutoff = datetime.utcnow() - timedelta(days=days)
        IntrusionLog.query.filter(IntrusionLog.timestamp < cutoff).delete()
        db.session.commit()


