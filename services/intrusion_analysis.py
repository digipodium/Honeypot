# MODULE 5 — ALERT GENERATION MODULE
# Generates alerts when suspicious patterns are detected
# ══════════════════════════════════════════════════════════════════

class AlertGenerator:
    """
    Evaluates each intrusion log and fires alerts when
    thresholds or patterns indicate real threat activity.
    """

    # How many hits from one IP in 10 minutes triggers brute-force alert
    BRUTE_FORCE_THRESHOLD = 10
    # How many criticals from one IP in 1 hour triggers escalation
    CRITICAL_THRESHOLD    = 3

    @staticmethod
    def evaluate(log: IntrusionLog):
        """
        Called after every logged event.
        Checks rules and inserts Alert records if triggered.
        """
        AlertGenerator._check_severity(log)
        AlertGenerator._check_brute_force(log)
        AlertGenerator._check_critical_repeat(log)

    @staticmethod
    def _check_severity(log: IntrusionLog):
        """Immediately alert on Critical events."""
        if log.severity == "critical":
            DatabaseManager.insert_alert({
                "title":       f"Critical threat detected — {log.threat_type}",
                "description": (
                    f"IP {log.ip_address} attempted {log.threat_type} on {log.path}. "
                    f"Action: {log.action_taken}."
                ),
                "ip_address":  log.ip_address,
                "severity":    "critical",
                "threat_type": log.threat_type,
            })
            print(f"[ALERT] CRITICAL — {log.ip_address} — {log.threat_type}")

    @staticmethod
    def _check_brute_force(log: IntrusionLog):
        """
        Alert if the same IP has hit 10+ times in the last 10 minutes.
        Classic brute-force or credential-stuffing pattern.
        """
        window = datetime.utcnow() - timedelta(minutes=10)
        count  = IntrusionLog.query.filter(
            IntrusionLog.ip_address == log.ip_address,
            IntrusionLog.timestamp  >= window
        ).count()

        if count == AlertGenerator.BRUTE_FORCE_THRESHOLD:
            DatabaseManager.insert_alert({
                "title":       f"Brute force pattern — {log.ip_address}",
                "description": (
                    f"IP {log.ip_address} made {count} requests in the last 10 minutes. "
                    f"Possible brute-force or automated scanner."
                ),
                "ip_address":  log.ip_address,
                "severity":    "high",
                "threat_type": "Brute Force",
            })
            print(f"[ALERT] BRUTE FORCE — {log.ip_address} ({count} hits / 10 min)")

    @staticmethod
    def _check_critical_repeat(log: IntrusionLog):
        """
        Alert if same IP generates 3+ critical events in 1 hour.
        Signals a persistent, targeted attacker.
        """
        window = datetime.utcnow() - timedelta(hours=1)
        count  = IntrusionLog.query.filter(
            IntrusionLog.ip_address == log.ip_address,
            IntrusionLog.severity   == "critical",
            IntrusionLog.timestamp  >= window
        ).count()

        if count == AlertGenerator.CRITICAL_THRESHOLD:
            DatabaseManager.insert_alert({
                "title":       f"Persistent attacker — {log.ip_address}",
                "description": (
                    f"IP {log.ip_address} has triggered {count} critical events in the last hour. "
                    f"Recommend immediate block."
                ),
                "ip_address":  log.ip_address,
                "severity":    "critical",
                "threat_type": "Persistent Attack",
            })
            # Auto-block persistent attackers
            DatabaseManager.block_ip(log.ip_address, reason="Auto-blocked: 3+ criticals in 1h", by="system")
            print(f"[ALERT] PERSISTENT ATTACKER auto-blocked — {log.ip_address}")


